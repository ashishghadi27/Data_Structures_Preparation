package Model;

public class Qts_list {
    private String qts;
    private String ans;

    public Qts_list(String qts, String ans) {
        this.qts = qts;
        this.ans = ans;
    }

    public String getQts() {
        return qts;
    }

    public String getAns() {
        return ans;
    }
}


